
public class OprEx03 {

	public static void main(String[] args) {

		int a = 12;
		int b = 20;
		int c = a & b;
		System.out.println(a+" & "+b+" : "+c);
		c = a | b;
		System.out.println(a+" | "+b+" : "+c);
		c = a ^ b;
		System.out.println(a+" ^ "+b+" : "+c);
		c = ~a;
		System.out.println("~a : "+c);
		
	}

}
