
public class WhileEx04 {

	public static void main(String[] args) {
		
		System.out.println("시침 -----------> 분침");
		for (int i = 1; i <= 5; i++) {
			for (int j = 1; j <= 5; j++) {
				System.out.println(i+" ---------------> "+j);
			}
			System.out.println("\n=====================\n");
		}
	}

}
