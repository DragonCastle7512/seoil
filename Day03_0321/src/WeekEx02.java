
public class WeekEx02 {

	public static void main(String[] args) {
		
		int a = 10, b = 3, res = 0;
		res = a+b;
		System.out.printf("%d + %d = %d\n", a, b, res);
		System.out.println(a+" - "+b+" = "+(a-b));
		res = a*b;
		System.out.println(a+" * "+b+" = "+(a*b));
		res = a/b;
		System.out.println(a+" / "+b+" = "+res);
	}

}
